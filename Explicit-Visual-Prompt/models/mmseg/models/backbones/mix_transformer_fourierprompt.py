class PatchEmbed(nn.Module):
    """ Image to Patch Embedding
    """

    def __init__(self, img_size=224, patch_size=16, in_chans=3, embed_dim=768):
        super().__init__()
        img_size = to_2tuple(img_size)
        patch_size = to_2tuple(patch_size)
        num_patches = (img_size[1] // patch_size[1]) * \
            (img_size[0] // patch_size[0])
        self.img_size = img_size
        self.patch_size = patch_size
        self.num_patches = num_patches

        self.proj = nn.Conv2d(in_chans, embed_dim,
                              kernel_size=patch_size, stride=patch_size)

    def forward(self, x):
        B, C, H, W = x.shape
        # FIXME look at relaxing size constraints
        assert H == self.img_size[0] and W == self.img_size[1], \
            f"Input image size ({H}*{W}) doesn't match model ({self.img_size[0]}*{self.img_size[1]})."

        x = self.proj(x)
        x = x.flatten(2).transpose(1, 2)

        return x


class FrequencyAdaptor(nn.Module):
    def __init__(self, hidden_dim, feat_dim, h, w, frequency_tune, embedding_tune, adaptor):
        """
        Args:
        """
        super(FrequencyAdaptor, self).__init__()
        self.hidden_dim = hidden_dim
        self.feat_dim = feat_dim
        self.frequency_tune = frequency_tune
        self.embedding_tune = embedding_tune
        self.h = h
        self.w = w
        self.adaptor = adaptor

        if self.frequency_tune:
            self.mlp_real_mask = nn.Linear(self.feat_dim, 1)
            self.mlp_imag_mask = nn.Linear(self.feat_dim, 1)
            self.sigmoid = nn.Sigmoid()

        if self.frequency_tune and self.embedding_tune:
            if adaptor:
                self.down = nn.Linear(self.feat_dim, self.hidden_dim)
                self.mlp_frequency = nn.Sequential(
                    nn.Linear(self.hidden_dim, self.hidden_dim),
                    nn.GELU(),
                )
                self.mlp_embedding = nn.Sequential(
                    nn.Linear(self.hidden_dim, self.hidden_dim),
                    nn.GELU(),
                )
                self.up = nn.Linear(self.hidden_dim, self.feat_dim)
            else:
                self.mlp_frequency = nn.Sequential(
                    nn.Linear(self.feat_dim, self.hidden_dim),
                    nn.GELU(),
                    nn.Linear(self.hidden_dim, self.feat_dim)
                )
                self.mlp_embedding = nn.Sequential(
                    nn.Linear(self.feat_dim, self.hidden_dim),
                    nn.GELU(),
                    nn.Linear(self.hidden_dim, self.feat_dim)
                )
        else:
            self.mlp = nn.Sequential(
                    nn.Linear(self.feat_dim, self.hidden_dim),
                    nn.GELU(),
                    nn.Linear(self.hidden_dim, self.feat_dim)
                )

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()

    def get_embedding(self, prompt):
        return prompt

    def get_frequency(self, prompt):
        fft_dim = (-2)
        ffted = torch.fft.rfftn(prompt, dim=fft_dim, norm='ortho')

        real_mask = self.sigmoid(self.mlp_real_mask(ffted.real))
        imag_mask = self.sigmoid(self.mlp_imag_mask(ffted.imag))
        real = ffted.real * real_mask
        imag = ffted.imag * imag_mask
        ffted = torch.complex(real, imag)

        ifft_shape_slice = prompt.shape[-2]
        freq_part = torch.fft.irfftn(ffted, s=ifft_shape_slice, dim=fft_dim, norm='ortho')

        self.real_mask = real_mask
        self.imag_mask = imag_mask

        return freq_part

    def forward(self, prompt):
        if self.embedding_tune and self.frequency_tune:
            if self.adaptor:
                prompt_frequency = self.get_frequency(prompt)
                prompt_embedding = self.get_embedding(prompt)
                prompt_frequency, prompt_embedding = self.down(prompt_frequency), self.down(prompt_embedding)
                prompt_frequency, prompt_embedding = self.mlp_frequency(prompt_frequency) + prompt_frequency, \
                                                     self.mlp_embedding(prompt_embedding) + prompt_embedding

                prompt = self.up(prompt_frequency + prompt_embedding)
                return prompt
            else:
                return self.mlp_frequency(self.get_frequency(prompt)) + self.mlp_embedding(prompt)
        elif self.frequency_tune:
            prompt = self.get_frequency(prompt)
            return self.mlp(prompt)
        elif self.embedding_tune:
            return self.mlp(prompt)


class PromptGenerator(nn.Module):
    def __init__(self, scale_factor, embed_dims, tuning_stage, depths,
                 frequency_tune, embedding_tune, adaptor, img_size):
        """
        Args:
        """
        super(PromptGenerator, self).__init__()
        self.scale_factor = scale_factor
        self.embed_dims = embed_dims
        self.tuning_stage = tuning_stage
        self.depths = depths
        self.frequency_tune = frequency_tune
        self.embedding_tune = embedding_tune
        self.adaptor = adaptor

        h, w = img_size//4, img_size//4
        if '1' in self.tuning_stage:
            for i in range(self.depths[0]):
                freq_adaptor = FrequencyAdaptor(self.embed_dims[0] // self.scale_factor, self.embed_dims[0], h, w,
                                                self.frequency_tune, self.embedding_tune, self.adaptor)
                setattr(self, 'freq_adaptor1_{}'.format(str(i)), freq_adaptor)

        h, w = h//2, w//2
        if '2' in self.tuning_stage:
            for i in range(self.depths[1]):
                freq_adaptor = FrequencyAdaptor(self.embed_dims[1] // self.scale_factor, self.embed_dims[1], h, w,
                                                self.frequency_tune, self.embedding_tune, self.adaptor)
                setattr(self, 'freq_adaptor2_{}'.format(str(i)), freq_adaptor)

        h, w = h//2, w//2
        if '3' in self.tuning_stage:
            for i in range(self.depths[2]):
                freq_adaptor = FrequencyAdaptor(self.embed_dims[2] // self.scale_factor, self.embed_dims[2], h, w,
                                                self.frequency_tune, self.embedding_tune, self.adaptor)
                setattr(self, 'freq_adaptor3_{}'.format(str(i)), freq_adaptor)

        h, w = h//2, w//2
        if '4' in self.tuning_stage:
            for i in range(self.depths[3]):
                freq_adaptor = FrequencyAdaptor(self.embed_dims[3] // self.scale_factor, self.embed_dims[3], h, w,
                                                self.frequency_tune, self.embedding_tune, self.adaptor)
                setattr(self, 'freq_adaptor4_{}'.format(str(i)), freq_adaptor)

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()

    def init_prompt(self, x):
        return x

    def get_prompt(self, prompt, block_num, depth_num):
        freq_adaptor = getattr(self, 'freq_adaptor' + str(block_num) + '_' + str(depth_num))
        prompt = freq_adaptor(prompt)

        return prompt


class DWConv(nn.Module):
    def __init__(self, dim=768):
        super(DWConv, self).__init__()
        self.dwconv = nn.Conv2d(dim, dim, 3, 1, 1, bias=True, groups=dim)

    def forward(self, x, H, W):
        B, N, C = x.shape
        x = x.transpose(1, 2).view(B, C, H, W)
        x = self.dwconv(x)
        x = x.flatten(2).transpose(1, 2)

        return x

